public class AllData {

	ChartData checkingData;

	ChartData savingsData;
	
	ChartData invData;
	
	ChartData loanData;
	
	ChartData creditData;
	
	ChartData netData;
	
	public ChartData getCheckingData() {
		return checkingData;
	}

	public void setCheckingData(ChartData checkingData) {
		this.checkingData = checkingData;
	}

	public ChartData getSavingsData() {
		return savingsData;
	}

	public void setSavingsData(ChartData savingsData) {
		this.savingsData = savingsData;
	}

	public ChartData getInvData() {
		return invData;
	}

	public void setInvData(ChartData invData) {
		this.invData = invData;
	}

	public ChartData getLoanData() {
		return loanData;
	}

	public void setLoanData(ChartData loanData) {
		this.loanData = loanData;
	}

	public ChartData getCreditData() {
		return creditData;
	}

	public void setCreditData(ChartData creditData) {
		this.creditData = creditData;
	}

	public ChartData getNetData() {
		return netData;
	}

	public void setNetData(ChartData netData) {
		this.netData = netData;
	}
}
